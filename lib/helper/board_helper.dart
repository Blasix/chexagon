bool isInBoard(int q, int r) {
  return q >= 0 &&
      q < 11 &&
      r >= 0 &&
      r < 11 &&
      !(r == 0 && q <= 4) &&
      !(r == 1 && q <= 3) &&
      !(r == 2 && q <= 2) &&
      !(r == 3 && q <= 1) &&
      !(r == 4 && q == 0) &&
      !(r == 6 && q == 10) &&
      !(r == 7 && q >= 9) &&
      !(r == 8 && q >= 8) &&
      !(r == 9 && q >= 7) &&
      !(r == 10 && q >= 6);
}

bool isPawnAtInitialPosition(int q, int r, bool isWhite) {
  return isWhite
      ? (q == 1 && r == 10) ||
          (q == 2 && r == 9) ||
          (q == 3 && r == 8) ||
          (q == 4 && r == 7) ||
          (q == 5 && r == 6) ||
          (q == 6 && r == 6) ||
          (q == 7 && r == 6) ||
          (q == 8 && r == 6) ||
          (q == 9 && r == 6)
      : (q == 1 && r == 4) ||
          (q == 2 && r == 4) ||
          (q == 3 && r == 4) ||
          (q == 4 && r == 4) ||
          (q == 5 && r == 4) ||
          (q == 6 && r == 3) ||
          (q == 7 && r == 2) ||
          (q == 8 && r == 1) ||
          (q == 9 && r == 0);
}
